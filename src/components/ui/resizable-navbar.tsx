export const NavbarButton = ({
  href,
  as: Tag = "a",
  children,
  className,
  variant = "primary",
  ...props
}: {
  href?: string;
  as?: React.ElementType;
  children: React.ReactNode;
  className?: string;
  variant?: "primary" | "secondary" | "dark" | "gradient";
} & (
  | React.ComponentPropsWithoutRef<"a">
  | React.ComponentPropsWithoutRef<"button">
)) => {
  const baseStyles =
    "px-4 py-2 rounded-sm text-sm font-regular relative cursor-pointer transition-all duration-300 inline-flex items-center gap-2 text-center";

  const variantStyles = {
    primary:
      "bg-gradient-to-r from-purple-600 to-blue-600 text-white hover:from-purple-700 hover:to-blue-700 font-light rounded-sm hover:shadow-lg hover:shadow-purple-500/50 hover:scale-105",
    secondary: "bg-transparent text-white border border-gray-600 hover:bg-gray-800/50",
    dark: "bg-gray-800 text-white hover:bg-gray-700 border border-gray-700",
    gradient:
      "bg-gradient-to-r from-purple-600 to-blue-600 text-white hover:from-purple-700 hover:to-blue-700 shadow-lg font-bold hover:shadow-lg hover:shadow-purple-500/50 hover:scale-105",
  };

  return (
    <Tag
      href={href || undefined}
      className={cn(baseStyles, variantStyles[variant], className)}
      {...props}
    >
      {children}
    </Tag>
  );
};
